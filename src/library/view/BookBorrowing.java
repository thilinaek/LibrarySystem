private void saveBookBorrowing() throws Exception{
          try {
        BookBorrowingDto dto = new BookBorrowingDto(txt_bookId.getText(), txt_studentId.getText(), date_issue.getDate(),date_return.getDate());

            String resp = bookBorrowingController.save(dto);

            JOptionPane.showMessageDialog(this, resp);
            clearForm();
           
    } catch (Exception e) {
        
        JOptionPane.showMessageDialog(this, "Error at save Data");

    }
            
          
}
 private void clearForm() {
        txt_bookId.setText("");
        txt_studentId.setText("");
        date_issue.setDate( null);
        date_return.setDate(null);
 }
  private void deleteBookBorrowing() throws Exception {
        try {
            String bookId = txt_bookId.getText();
            String resp = bookBorrowingController.delete(bookId);
            JOptionPane.showMessageDialog(this, resp);
            clearForm();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error at Delete BookBorrowing");
        }
    }

    

private void saveBook() throws Exception{
          try {
        BookDto dto = new BookDto(txtBookId.getText(), txtBookName.getText(), txtAuthor.getText());

            String resp = bookController.save(dto);

            JOptionPane.showMessageDialog(this, resp);
            clearForm();
            loadTable();
    } catch (Exception e) {
        
        JOptionPane.showMessageDialog(this, "Error at save Data");

    }
            
          
}
 private void clearForm() {
        txtBookId.setText("");
        txtBookName.setText("");
        txtAuthor.setText("");
 }
 private void loadTable() throws Exception {
      try {
            String columns[] = {"Book Id", "Name", "Author"};
            DefaultTableModel dtm = new DefaultTableModel(columns, 0) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };
            tblBook.setModel(dtm);
            tblBook.revalidate();
            tblBook.repaint();
            


            ArrayList<BookDto> bookDtos = bookController.getAll();
            
           for (BookDto dto : bookDtos) {
                Object[] rowData = {dto.getBookId(), dto.getName(), dto.getAuthor()};
                dtm.addRow(rowData);
            }



      } catch (Exception e) {
            
            JOptionPane.showMessageDialog(this, "Error at Loading Data to Book Table");
     }
    }

    private void searchBook() throws Exception {
       try {
            String bookId = tblBook.getValueAt(tblBook.getSelectedRow(), 0).toString();
            BookDto dto = bookController.get(bookId);

            if (dto != null) {
                txtBookId.setText(dto.getBookId());
                txtBookName.setText(dto.getName());
                txtAuthor.setText(dto.getAuthor());

            } else {
                JOptionPane.showMessageDialog(this, "Book Not Found");
            }

       } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error at Book");
       }
    }

    private void deleteBook() throws Exception {
        try {
            String bookId = txtBookId.getText();
            String resp = bookController.delete(bookId);
            JOptionPane.showMessageDialog(this, resp);
            clearForm();
            loadTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error at Delete Book");
        }
    }

    private void updateBook() throws Exception {
        try {
            BookDto bookDto = new BookDto(txtBookId.getText(), txtBookName.getText(), txtAuthor.getText());
            String resp = bookController.update(bookDto);
            JOptionPane.showMessageDialog(this, resp);
            loadTable();
            clearForm();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error at update Book");
        }
    }

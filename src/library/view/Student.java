private void saveStudent() throws Exception{
         try {
        StudentDto dto = new StudentDto(txtStuId.getText(), txtStuName.getText(), txtEmail.getText(), Integer.valueOf(txtContactNo.getText()),txtGender.getText());

            String resp = studentController.save(dto);

            JOptionPane.showMessageDialog(this, resp);
            clearForm();
            loadTable();
  } catch (Exception e) {
        
        JOptionPane.showMessageDialog(this, "Error at save Data");

   }
            
    }
 private void clearForm() {
        txtStuId.setText("");
        txtStuName.setText("");
        txtEmail.setText("");
        txtContactNo.setText("");
        txtGender.setText("");

    }


private void loadTable() throws Exception {
      try {
            String columns[] = {"StudentId", "StudentName", "Email", "ContactNo","Gender"};
            DefaultTableModel dtm = new DefaultTableModel(columns, 0) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };
            tblstudent.setModel(dtm);
            tblstudent.revalidate();
            tblstudent.repaint();
            


            ArrayList<StudentDto> studentDtos = studentController.getAll();
            
           for (StudentDto dto : studentDtos) {
                Object[] rowData = {dto.getStudentId(), dto.getStudentName(), dto.getEmail(), dto.getContactNo(),dto.getGender()};
                dtm.addRow(rowData);
            }



       } catch (Exception e) {
            
            JOptionPane.showMessageDialog(this, "Error at Loading Data to Student Table");
        }
    }
 private void searchStudent() throws Exception {
       try {
            String bookId = tblstudent.getValueAt(tblstudent.getSelectedRow(), 0).toString();
            StudentDto dto = studentController.get(bookId);

            if (dto != null) {
                txtStuId.setText(dto.getStudentId());
                txtStuName.setText(dto.getStudentName());
                txtEmail.setText(dto.getEmail());
                txtContactNo.setText(Integer.toString(dto.getContactNo()));
                txtGender.setText(dto.getGender());

            } else {
                JOptionPane.showMessageDialog(this, "Student Not Found");
            }

       } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error at loading Student");
       }
    }

    private void deleteStudent() throws Exception {
        try {
            String studentId = txtStuId.getText();
            String resp = studentController.delete(studentId);
            JOptionPane.showMessageDialog(this, resp);
            clearForm();
            loadTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error at Delete Student");
        }
    }

    private void updateStudent() throws Exception {
        try {
            StudentDto studentDto = new StudentDto(
                    txtStuId.getText(), 
                    txtStuName.getText(),
                    txtEmail.getText(),
                     Integer.valueOf(txtContactNo.getText()),
                    txtGender.getText());
                    
            String resp = studentController.update(studentDto);
            JOptionPane.showMessageDialog(this, resp);
            loadTable();
            clearForm();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error at update Student");
        }
    }
